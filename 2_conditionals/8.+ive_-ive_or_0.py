'''Create a program that determines if a number is positive, negative, or zero.'''

#Answer:

#Get the input from the user ans store it.
number=int(input("Enter a number: "))

#check if the number is +ive, -ive or 0 and by using if-elif-else statement.
if number>0:
    print("The number is positive(+ive)")
elif number<0:
    print("The number is negative(-ive)")
else:
    print("The number is zero")

